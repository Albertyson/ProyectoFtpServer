    public boolean hayUsuarios() {
        boolean hay = false;
        try {
            FileInputStream entrada = new FileInputStream(archUsers);
            ObjectInputStream objeto = new ObjectInputStream(entrada);
            try {
                if ((User) objeto.readObject() == null) {
                    hay = false;
                } else {
                    hay = true;
                }
            } catch (EOFException eofex) {
            } finally {
                entrada.close();
                objeto.close();
            }
        } catch (Exception ex) {
        }
        return hay;
    }

    public void cargarUsuarios() {
        User temp;
        usersList = new ArrayList();
        try {
            FileInputStream entrada = new FileInputStream(archUsers);
            ObjectInputStream objeto = new ObjectInputStream(entrada);
            try {
                while ((temp = (User) objeto.readObject()) != null) {
                    usersList.add(temp);
                }
            } catch (EOFException e) {
            } finally {
                objeto.close();
                entrada.close();
            }
        } catch (Exception e) {
        }
    }
